#!/usr/bin/python3.12

# ========================================String Concatenation
print("\n========================================|||:String Concatenation")

str1 = "Hello"
str2 = "World"
blank = " "
print("String 1:", str1)
print("String 2:", str2)
str3 = str1 + blank + str2
print("String 3:", str3)

# ========================================Multiplying
print("\n========================================|||:Multiplying")

str1 = "Hello"
str2 = "World"
print("String 1:", str1)
print("String 2:", str2)
str3 = str1 + str2 * 3
print("String 3:", str3)
str4 = (str1 + str2) * 3
print("String 4:", str4)
